import { type ClassValue, clsx } from "clsx";
import { twMerge } from "tailwind-merge";

export function cn(...inputs: ClassValue[]) {
  return twMerge(clsx(inputs));
}

export function formatCurrency(value: number): string {
  return new Intl.NumberFormat('pt-BR', {
    style: 'currency',
    currency: 'BRL'
  }).format(value);
}

export function formatDate(date: Date | string): string {
  const d = typeof date === 'string' ? new Date(date) : date;
  return new Intl.DateTimeFormat('pt-BR').format(d);
}

export function getInitials(name: string): string {
  if (!name) return '';
  return name
    .split(' ')
    .map(part => part[0])
    .join('')
    .toUpperCase()
    .substring(0, 2);
}

export function calculateTotalPrice(quantity: number, price: number): number {
  return quantity * price;
}

export function isLowStock(quantity: number, minQuantity: number): boolean {
  return quantity <= minQuantity;
}

export function getStockStatusLabel(quantity: number, minQuantity: number): string {
  if (quantity <= 0) return 'Sem estoque';
  if (quantity <= minQuantity) return 'Estoque baixo';
  return 'Normal';
}

export function getStockStatusClass(quantity: number, minQuantity: number): string {
  if (quantity <= 0) return 'bg-red-100 text-red-800 dark:bg-red-900 dark:text-red-200';
  if (quantity <= minQuantity) return 'bg-amber-100 text-amber-800 dark:bg-amber-900 dark:text-amber-200';
  return 'bg-green-100 text-green-800 dark:bg-green-900 dark:text-green-200';
}

